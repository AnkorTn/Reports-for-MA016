function N_Logistic(a,x0)
    x1(1) = x0;
    x2(1) = x0+0.0001;
    y1(1) = 1;
    % i 的范围可以操控，这里通过数组的方式从2->N，N取2000
    for i = 2:10000
        x2(i) = a*x2(i-1)*(1-x2(i-1));
        x1(i) = a*x1(i-1)*(1-x1(i-1));
        y1(i) = i;
    end
    plot(y1,x1,'r.');
    hold on
    plot(y1,x2,'b.');
    x2(100)-x1(100)
    x2(500)-x1(500)
    x2(1000)-x1(1000)
    x2(1500)-x1(1500)
    x2(2000)-x1(2000)
    x2(4000)-x1(4000)
    x2(5000)-x1(5000)
    x2(8000)-x1(8000)
    x2(9000)-x1(9000)
    x2(10000)-x1(10000)